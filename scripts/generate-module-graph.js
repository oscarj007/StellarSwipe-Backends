#!/usr/bin/env node
/**
 * Module Dependency Graph Generator
 *
 * Parses @Module decorator `imports` arrays across src/ to build a
 * Mermaid dependency graph. Saves output to docs/module-graph.md.
 *
 * Usage:  npm run docs:module-graph
 */

const fs = require('fs');
const path = require('path');

const SRC_DIR = path.resolve(__dirname, '../src');
const OUT_FILE = path.resolve(__dirname, '../docs/module-graph.md');

// ── helpers ──────────────────────────────────────────────────────────────────

function findModuleFiles(dir, results = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      findModuleFiles(full, results);
    } else if (entry.isFile() && entry.name.endsWith('.module.ts')) {
      results.push(full);
    }
  }
  return results;
}

/**
 * Extract the module class name and the identifiers listed in `imports: [...]`
 * from a raw TypeScript source string.
 */
function parseModule(source, filePath) {
  // Module class name
  const classMatch = source.match(/export\s+class\s+(\w+Module)\b/);
  if (!classMatch) return null;
  const moduleName = classMatch[1];

  // Find @Module({ ... imports: [ ... ] ... })
  // We use a simple brace-depth scanner to handle multi-line arrays.
  const moduleDecoratorIdx = source.indexOf('@Module(');
  if (moduleDecoratorIdx === -1) return { name: moduleName, imports: [] };

  const afterDecorator = source.slice(moduleDecoratorIdx);
  const importsMatch = afterDecorator.match(/imports\s*:\s*\[/);
  if (!importsMatch) return { name: moduleName, imports: [] };

  const arrayStart = afterDecorator.indexOf('[', importsMatch.index);
  let depth = 0;
  let arrayEnd = arrayStart;
  for (let i = arrayStart; i < afterDecorator.length; i++) {
    if (afterDecorator[i] === '[') depth++;
    else if (afterDecorator[i] === ']') {
      depth--;
      if (depth === 0) { arrayEnd = i; break; }
    }
  }

  const arrayContent = afterDecorator.slice(arrayStart + 1, arrayEnd);

  // Extract identifiers that look like *Module (ignore .forRoot(), .register(), etc.)
  const importedModules = [];
  const identifierRe = /\b(\w+Module)\b/g;
  let m;
  while ((m = identifierRe.exec(arrayContent)) !== null) {
    if (m[1] !== moduleName) importedModules.push(m[1]);
  }

  return { name: moduleName, imports: importedModules };
}

// ── main ─────────────────────────────────────────────────────────────────────

const files = findModuleFiles(SRC_DIR);
const graph = {}; // moduleName → Set<importedModuleName>

for (const file of files) {
  const source = fs.readFileSync(file, 'utf8');
  const info = parseModule(source, file);
  if (!info) continue;
  if (!graph[info.name]) graph[info.name] = new Set();
  for (const dep of info.imports) {
    graph[info.name].add(dep);
  }
}

// ── detect circular dependencies ─────────────────────────────────────────────

function detectCycles(graph) {
  const cycles = [];
  const visited = new Set();
  const stack = new Set();

  function dfs(node, path) {
    if (stack.has(node)) {
      const cycleStart = path.indexOf(node);
      cycles.push(path.slice(cycleStart).concat(node));
      return;
    }
    if (visited.has(node)) return;
    visited.add(node);
    stack.add(node);
    for (const neighbour of (graph[node] || [])) {
      dfs(neighbour, path.concat(node));
    }
    stack.delete(node);
  }

  for (const node of Object.keys(graph)) {
    dfs(node, []);
  }
  return cycles;
}

const cycles = detectCycles(graph);
const cycleNodes = new Set(cycles.flat());

// ── build Mermaid output ──────────────────────────────────────────────────────

const lines = ['graph LR'];

for (const [mod, deps] of Object.entries(graph)) {
  if (deps.size === 0) {
    // Standalone node
    lines.push(`  ${mod}`);
  }
  for (const dep of deps) {
    lines.push(`  ${mod} --> ${dep}`);
  }
}

// Mark circular nodes with a distinct style
if (cycleNodes.size > 0) {
  lines.push('');
  lines.push('  %% Circular dependency nodes highlighted in red');
  for (const node of cycleNodes) {
    lines.push(`  style ${node} fill:#f66,color:#000`);
  }
}

const mermaidBlock = ['```mermaid', ...lines, '```'].join('\n');

const cycleSection = cycles.length === 0
  ? '> ✅ No circular dependencies detected.'
  : [
      '> ⚠️ **Circular dependencies detected** — see red nodes in the graph above.',
      '',
      '| Cycle |',
      '|-------|',
      ...cycles.map(c => `| ${c.join(' → ')} |`),
    ].join('\n');

const output = `# Module Dependency Graph

> Auto-generated by \`npm run docs:module-graph\`. Do not edit manually.

${cycleSection}

## Graph

${mermaidBlock}
`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, output, 'utf8');

console.log(`✔  Module graph written to ${OUT_FILE}`);
console.log(`   Modules analysed : ${Object.keys(graph).length}`);
console.log(`   Edges            : ${Object.values(graph).reduce((n, s) => n + s.size, 0)}`);
if (cycles.length > 0) {
  console.warn(`   ⚠  Circular deps  : ${cycles.length} cycle(s) found`);
  for (const c of cycles) console.warn(`      ${c.join(' → ')}`);
  process.exit(1);
}
